// Double Function
function double(n) {
  return n * 2;
}
console.log("double(2) =", double(2));      // Testing

// Square Function
function square(n) {
  return n * n;
}
console.log("square(3) =", square(3));      // Testing

// IsEven Function
function isEven(n) {
  return n % 2 === 0;
}
console.log("isEven(4) =", isEven(4));        // Testing
console.log("isEven(5) =", isEven(5));        // Testing

// IsOdd Function
function isOdd(n) {
  return n % 2 !== 0;
}
console.log("isOdd(4) =", isOdd(4));          // Testing
console.log("isOdd(5) =", isOdd(5));          // Testing

// Multiply Function
function multiply(a, b) {
  return a * b;
}
console.log("multiply(2, 3) =", multiply(2, 3)); // Testing
